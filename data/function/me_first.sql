CREATE OR REPLACE FUNCTION "getonlinetime"()
  RETURNS "pg_catalog"."timestamp" AS $BODY$
declare
  onlinetime TIMESTAMP;
begin
  onlinetime = now() + '- 5 min';
  return onlinetime;
end;

$BODY$
  LANGUAGE plpgsql VOLATILE
  COST 100;

ALTER FUNCTION "getonlinetime"() OWNER TO "postgres";
